// ============================================================
// i18n — Internationalization module for Mieru-OCR
// Supports: zh (Chinese), ja (Japanese), en (English)
// ============================================================

export type Locale = 'zh' | 'ja' | 'en';
export type LocaleOption = 'auto' | Locale;

let currentLocale: Locale = 'en';

// --------------- language detection ---------------

function detectLocale(): Locale {
  try {
    // Extension environment
    if (typeof chrome !== 'undefined' && chrome.i18n && typeof chrome.i18n.getUILanguage === 'function') {
      const lang = chrome.i18n.getUILanguage().toLowerCase();
      if (lang.startsWith('zh')) return 'zh';
      if (lang.startsWith('ja')) return 'ja';
      return 'en';
    }
  } catch { /* ignore */ }

  try {
    // Userscript / general browser environment
    const lang = (navigator.language || (navigator as any).userLanguage || '').toLowerCase();
    if (lang.startsWith('zh')) return 'zh';
    if (lang.startsWith('ja')) return 'ja';
  } catch { /* ignore */ }

  return 'en';
}

export function initLocale(option?: LocaleOption): void {
  if (option && option !== 'auto') {
    currentLocale = option;
  } else {
    currentLocale = detectLocale();
  }
}

export function setLocale(locale: Locale): void {
  currentLocale = locale;
}

export function getCurrentLocale(): Locale {
  return currentLocale;
}

// --------------- translation function ---------------

export function t(key: string, ...args: (string | number)[]): string {
  const dict = messages[currentLocale] || messages.en;
  let text = dict[key] ?? messages.en[key] ?? key;
  for (let i = 0; i < args.length; i++) {
    text = text.replace(`{${i}}`, String(args[i]));
  }
  return text;
}

// --------------- DOM translation ---------------

export function translatePage(root?: Element | Document): void {
  const container = root || document;

  container.querySelectorAll('[data-i18n]').forEach((el) => {
    const key = el.getAttribute('data-i18n');
    if (key) el.textContent = t(key);
  });

  container.querySelectorAll('[data-i18n-placeholder]').forEach((el) => {
    const key = el.getAttribute('data-i18n-placeholder');
    if (key) (el as HTMLInputElement).placeholder = t(key);
  });

  container.querySelectorAll('[data-i18n-title]').forEach((el) => {
    const key = el.getAttribute('data-i18n-title');
    if (key) (el as HTMLElement).title = t(key);
  });

  container.querySelectorAll('[data-i18n-html]').forEach((el) => {
    const key = el.getAttribute('data-i18n-html');
    if (key) el.innerHTML = t(key);
  });

  container.querySelectorAll('[data-i18n-aria-label]').forEach((el) => {
    const key = el.getAttribute('data-i18n-aria-label');
    if (key) el.setAttribute('aria-label', t(key));
  });
}

